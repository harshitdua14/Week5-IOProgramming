package json.com.validate_email;

public class User {
	private String name;
	private String age;
	private String email;
	
	void setName(String name) {
		this.name = name;
	}
	
	String getName() {
		return name;
	}
	
	void setAge(String age) {
		this.age = age;
	}
	
	String getAge() {
		return age;
	}
	
	void setEmail(String email) {
		this.email = email;
	}
	
	String getEmail() {
		return email;
	}
}
